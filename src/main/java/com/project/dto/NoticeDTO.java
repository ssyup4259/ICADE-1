package com.project.dto;

public class NoticeDTO {
	private int BN_NUM;
	private String BN_SUBJECT;
	private String BN_CONTENT;
	private String BN_DATE;

	public int getBN_NUM() {
		return BN_NUM;
	}

	public void setBN_NUM(int bN_NUM) {
		BN_NUM = bN_NUM;
	}

	public String getBN_SUBJECT() {
		return BN_SUBJECT;
	}

	public void setBN_SUBJECT(String bN_SUBJECT) {
		BN_SUBJECT = bN_SUBJECT;
	}

	public String getBN_CONTENT() {
		return BN_CONTENT;
	}

	public void setBN_CONTENT(String bN_CONTENT) {
		BN_CONTENT = bN_CONTENT;
	}

	public String getBN_DATE() {
		return BN_DATE;
	}

	public void setBN_DATE(String bN_DATE) {
		BN_DATE = bN_DATE;
	}

}
